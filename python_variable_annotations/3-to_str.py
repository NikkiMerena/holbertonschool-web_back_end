#!/usr/bin/env python3
"""This module contains a function that converts a float to its string rep."""


def to_str(n: float) -> str:
    """Takes a float n as argument and returns the string rep of the float.

    Args:
        n (float): the float to convert to a string.

    Returns:
        str: the string representation of the float.
    """
    return str(n)
